package com.vm.tranning.java.strings;

public class StringMethods {
public static void main(String[] args) {
	String s ="Value Momentum";
	System.out.println(s.charAt(0));
	System.out.println(s.compareTo("Teja"));
	System.out.println(s.concat("haii"));
    System.out.println(s.contains("Value"));
    System.out.println(s.endsWith("tum"));
    System.out.println(s.equals("Value Momentum"));
    System.out.println(s.indexOf(97));
    System.out.println(s.isEmpty());
    System.out.println(s.length());
    System.out.println(s.startsWith("Value"));
}
}
